int fibonacci(int f){
  int fibo[f] = {};
  fibo[0] = 1;
  fibo[1] = 1;
  for (int i = 2; i < f; i++)
    fibo[i] = fibo[i-1]+fibo[-2];
  return fibo[f];
}

